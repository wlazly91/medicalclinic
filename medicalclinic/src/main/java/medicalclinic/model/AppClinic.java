package medicalclinic.model;

public class AppClinic {
	
	private String who;
	private int id;
	
	public AppClinic() {}
	
	public void setWho(String who) {
		this.who = who;
	}
	
	public String getWho() {
		return who;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public int getId() {
		return id;
	}
}
